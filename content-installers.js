const container = document.querySelector('#card-container');

// Iterate through all the installers
installers.forEach((installer) => {
    // Create elements
    const card = document.createElement('div');
    const title = document.createElement('h3');
    const picture = document.createElement('picture');
    const image = document.createElement('img');
    const description = document.createElement('p');
    const download_link = document.createElement('a');
    const website_link = document.createElement('a');

    card.setAttribute('class','card');
    title.textContent = installer.name;
    image.setAttribute('src',`installers/images/${installer.image}`);
    description.innerHTML = installer.description;
    description.setAttribute('class','card-description');
    download_link.setAttribute('href', installer.download_link);
    download_link.innerHTML = 'Download Link &RightArrow;';
    website_link.setAttribute('href',installer.website_link);
    website_link.innerHTML = 'Go to Website &RightArrow;';

    // Append elements
    card.appendChild(title);
    card.appendChild(picture);
    picture.appendChild(image);
    card.appendChild(description);
    card.appendChild(download_link);
    card.appendChild(website_link);

    // Append card to container
    container.appendChild(card);
})
